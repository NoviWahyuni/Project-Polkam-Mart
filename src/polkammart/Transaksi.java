    private void kd_transaksi(){ 
        try{
            java.util.Date tgl = new java.util.Date();
            java.text.SimpleDateFormat kal = new java.text.SimpleDateFormat("yyMMdd");
            java.text.SimpleDateFormat tanggal = new java.text.SimpleDateFormat("yyyyMMdd");
            
            String sql = "Select max(no_transaksi) from transaksi where tanggal =" + tanggal.format(tgl);
            st = cn.createStatement();
            rs = st.executeQuery(sql);
            while(rs.next()){
                Long a = rs.getLong(1);
                if (a == 0) {
                    this.KodeTransaksi.setText(kal.format(tgl)+"0000"+(a+1));
                } else {
                    this.KodeTransaksi.setText(""+(a+1));
                }
            }
        }catch(Exception e){
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }
        
    public void Barang(){
        NamaBarang.removeAllItems();
        NamaBarang.addItem("Pilih Barang");
        try {
            st = cn.createStatement();
            rs = st.executeQuery("select * from barang");
            while (rs.next()) {
                NamaBarang.addItem(rs.getString("nama_barang"));
            }
        } catch (Exception e) { 
            e.printStackTrace();
        }
    }
        
