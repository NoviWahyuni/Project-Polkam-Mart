    private void HitungActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_HitungActionPerformed
        // TODO add your handling code here:
        if("".equals(HargaBarang.getText())||"".equals(Stok.getText())||"".equals(JumlahBarang.getText())){
            JOptionPane.showMessageDialog(null, "Lengkapi Data Anda");
        }else{
            Integer jumlah = Integer.parseInt(JumlahBarang.getText());
            Integer stok = Integer.parseInt(this.Stok.getText());
            if (jumlah > stok){
                JOptionPane.showMessageDialog(null, "Stok Hanya Ada "+stok);
            }else{
                int a = Integer.parseInt(HargaBarang.getText());
        int b = Integer.parseInt(JumlahBarang.getText());
        int hitung = a * b;
        Harga.setText(""+hitung);
        Tambah.setEnabled(true);
        Hitung.setEnabled(true);
            }   
        }
    }//GEN-LAST:event_HitungActionPerformed

