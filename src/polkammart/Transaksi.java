    private void NamaBarangActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_NamaBarangActionPerformed
        // TODO add your handling code here:
        try {
            HargaBarang.setText("");
            Stok.setText("");
            st = cn.createStatement();
            rs = st.executeQuery("select * from barang where nama_barang = '"+ NamaBarang.getSelectedItem() +"'");
            while (rs.next()) {
                if ("Pilih Barang".equals(NamaBarang.getSelectedItem())) {
                    HargaBarang.setText("");
                    Stok.setText("");
                } else {
                    Barcode.setText(rs.getString("barcode"));
                    HargaBarang.setText(rs.getString("harga"));
                    Stok.setText(rs.getString("stok"));
                }
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, e.getMessage());
        }
    }//GEN-LAST:event_NamaBarangActionPerformed

